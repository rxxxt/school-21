#include "libft.h"

int	ft_nbrlen(const char *s)
{
	int	count;

	count = 0;
	if (s)
	{
		while (*s == '0')
			s++;
		while (ft_isdigit(*s) && s++)
			count++;
	}
	return (count);
}
