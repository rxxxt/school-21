#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char			*new;
	unsigned int	i;

	if (!(new = malloc(sizeof(char) * (ft_strlen(s1) + 1))))
		return (0);
	i = 0;
	while (s1[i])
	{
		new[i] = s1[i];
		i++;
	}
	new[i] = '\0';
	return (new);
}
